        await showDialog(
          context: context,
          builder: (alertDialogContext) {
            return AlertDialog(
              title: Text('登入失敗'),
              actions: [
                TextButton(
                  onPressed: () =>
                      Navigator.pop(alertDialogContext),
                  child: Text('Ok'),
                ),
              ],
            );
          },
        );
      }
      else{
        await showDialog(
          context: context,
          builder: (alertDialogContext) {
            return AlertDialog(
              title: Text('登入成功'),
              actions: [
                TextButton(
                  onPressed: () =>
                      Navigator.pop(alertDialogContext),
                  child: Text('Ok'),
                ),
              ],
            );
          },
        );
        context.pushNamed('home');
      }
    }
